public class Numbers
{
	public int FindMinumum(int number1, int number2)
	{
		return number1 + number2;
		
	}
	public int FindMinumum(int number1, int number2), float number3)
	{
		return number1 + number2 + number3;
		
	}
	public int FindMinumum(int number1, int number2)
	{
		return number1 - number2;
		
	}
	public int FindMinumum(int number1, int number2, float number3)
	{
		return number1 + number2 - number3;
		
		
	}
	
}